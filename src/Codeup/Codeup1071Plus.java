package Codeup;

import java.util.Scanner;

public class Codeup1071Plus {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int iVal;
        do {
            iVal = sc.nextInt();
            if (iVal != 0) {
                System.out.println(iVal);
            }
        } while (iVal != 0);
    }
}
